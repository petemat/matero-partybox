export const DEFAULT_PACKS: PackId[] = ["classic"];

export function isPackId(v: unknown): v is PackId {
  return v === "classic" || v === "movies" || v === "internet" || v === "kids" || v === "hard";
}

export function normalizePackSelection(sel: unknown): PackId[] {
  if (!Array.isArray(sel)) return DEFAULT_PACKS;
  const ids = sel.filter(isPackId);
  return ids.length ? Array.from(new Set(ids)) : DEFAULT_PACKS;
}
